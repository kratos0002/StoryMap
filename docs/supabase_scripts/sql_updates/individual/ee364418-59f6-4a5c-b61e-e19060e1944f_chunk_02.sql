"I''m not a collector," said I. — "I was young then," he went on.
      "Lord! how I used to fly round." He seemed to take my measure. "I was in
      the East Indies two years, and in Brazil seven. Then I went to
      Madagascar."

"Butcher—Butcher?" The name seemed vaguely present in my memory;
      then I recalled Butcher v. Dawson. "Why!" said I, "you are
